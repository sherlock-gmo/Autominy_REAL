#  		               TIP
def tip(imagenN):
	H=np.array([[5.57683455e-01, 1.67822252, -6.18197647e+01],[1.87487880e-01, 5.62568939, 2.61678382e-01],[5.73734464e-04, 1.67143035e-02, 1.0]]) 
	imagenH = cv2.warpPerspective(imagenN, H, (200,300),borderMode=cv2.BORDER_CONSTANT, borderValue=(0, 0, 0)) 
	return imagenH
#*************************************************************************************************************
